void solve() {
	string s1, s2;
	cin >> s1;
	cin >> s2;
	dbg(s1);
	dbg(s2);
	if (s1.size() > s2.size()) {
		cout << "false s2 is smaller than s1\n";
		return ;
	}
	vector<int> s1_nums(26);
	vector<int> s2_nums(26);
	for (auto it : s1)
		s1_nums[it - 'a']++;
	int i = 0, j = 0;
	int k = s1.size();
	while (j < s2.size()) {
		while (j - i + 1 < k) {
			if ((s2[i] - 'a' >= 0) && (s2[i] - 'a') < 26) {
				s2_nums[s2[j] - 'a']++;
			}
			j++;
		}
		s2_nums[s2[j] - 'a']++;
		if (s1_nums == s2_nums) {
			cout << "true - permutaion  matched\n";
			return ;
		}
		else {
			if ((s2[i] - 'a' >= 0) && (s2[i] - 'a') < 26) {
				s2_nums[s2[i] - 'a']--;
			}

			++i;
			++j;
		}
	}
	cout << "false - permutaion not matched\n";
	return ;
}
